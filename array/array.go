package array

/*
Массив — это структура данных, которая представляет собой последовательный набор элементов
одинакового типа, хранящихся в непрерывной области памяти. В языке Go массивы имеют фиксированную
длину, которая задаётся при их создании и не может быть изменена в процессе выполнения программы.
Пример массива чисел: [5]int{1, 2, 3, 4, 5}. Массивы полезны, когда заранее известно точное
количество элементов, но из-за своей статичности они менее гибкие по сравнению со срезами.

Описание массива (Array):
- len: длина массива — количество элементов, которое он содержит. В случае массива длина
  фиксирована и определяется при объявлении (например, [5]int имеет len = 5).
- capacity: вместимость массива совпадает с его длиной, так как массив не может "вырасти"
  сверх заданного размера. Таким образом, capacity = len.
- type: тип данных элементов массива, который должен быть одинаковым для всех элементов
  (например, int, string и т.д.).
- Особенности: Массивы в Go являются значимыми типами (value types), то есть при присваивании
  или передаче в функцию создаётся копия всего массива.

Пример массива:
var arr [3]int = [3]int{1, 2, 3} // Массив из трёх целых чисел.

---

Срез (Slice) — это более гибкая и динамическая структура данных, построенная поверх массива.
Срез не имеет фиксированной длины, а представляет собой "окно" в базовый массив, которое может
изменяться в размере. Срез состоит из указателя на базовый массив, длины (len) и вместимости
(capacity). Это делает срезы основным инструментом для работы с последовательностями данных в Go.

Описание среза (Slice):
- len: текущая длина среза — количество элементов, доступных в данный момент через срез.
  Может быть меньше или равно capacity.
- capacity: вместимость среза — количество элементов в базовом массиве, начиная с позиции,
  на которую указывает срез, до конца базового массива. Определяет, сколько элементов можно
  добавить в срез без выделения новой памяти.
- type: тип данных элементов среза, как и у массива, должен быть одинаковым.
- Особенности: Срезы являются ссылочными типами (reference types), то есть они ссылаются на
  базовый массив, и изменения в срезе отражаются в этом массиве. При превышении capacity
  автоматически создаётся новый базовый массив большего размера, а срез начинает ссылаться на него.

Пример среза:
slice := []int{1, 2, 3} // Срез, созданный без явного указания длины.
slice = append(slice, 4) // Добавление элемента, длина увеличивается.

Различия между массивом и срезом:
1. Массив имеет фиксированную длину, срез — динамическую.
2. Массив копируется при передаче, срез передаёт ссылку на базовый массив.
3. Срезы поддерживают операции вроде append(), которые недоступны для массивов.

Таким образом, массивы подходят для статичных данных с известным размером, а срезы — для
динамических данных, где размер может изменяться в процессе работы программы.
*/
