package array

/*
Array (Массив)

Что это такое?
Массив — это коллекция элементов ОДНОГО типа с ФИКСИРОВАННЫМ размером.
Элементы массива хранятся в памяти последовательно, друг за другом.

Зачем это нужно?
- Максимально эффективное использование памяти (нет оверхеда на заголовки слайсов, если массив небольшой).
- Гарантия фиксированного размера (нельзя случайно добавить лишний элемент).
- Используется как "building block" для создания срезов (slices).

В чём смысл?
- Быстрый доступ по индексу O(1).
- Локальность данных в кэше процессора (CPU cache friendly).
- Размер является частью типа (например, [3]int и [5]int — это РАЗНЫЕ типы данных).

Когда использовать?
- Когда точно известно количество элементов и оно не будет меняться (например, матрица 3x3, UUID из 16 байт).
- В низкоуровневом коде для оптимизации.
- В большинстве прикладных задач в Go предпочтительнее использовать Slices (Срезы).

Как работает?
- Память выделяется сразу под все элементы.
- При передаче в функцию массив КОПИРУЕТСЯ целиком (pass by value), если не использовать указатель.

Примеры использования:
*/

func ArrayExamples() {
	// Создание массива из 5 целых чисел
	var arr1 [5]int              // [0 0 0 0 0] - инициализация нулями
	arr2 := [3]int{1, 2, 3}      // [1 2 3] - литерал
	arr3 := [...]int{1, 2, 3, 4} // [1 2 3 4] - компилятор сам считает длину
	var arr4 [2]string = [2]string{"hello", "world"}

	// Доступ к элементам - O(1)
	firstElement := arr2[0]
	lastElement := arr2[2]

	// Изменение элемента - O(1)
	arr2[1] = 10 // [1 10 3]

	// Длина массива (константа)
	length := len(arr2) // 3

	// Чтобы компилятор не ругался на неиспользуемые переменные
	_, _, _, _, _, _, _ = arr1, arr2, arr3, arr4, firstElement, lastElement, length
}
