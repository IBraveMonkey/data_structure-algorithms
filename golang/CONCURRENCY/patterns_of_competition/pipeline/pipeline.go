package main

import "fmt"

/*
 * Паттерн Pipeline (Конвейер) используется для создания цепочки обработчиков данных, где каждый этап обрабатывает данные и передает их на следующий этап.
 * Он позволяет разбить сложную обработку на несколько простых этапов.
 * Пример использования: обработка изображений, где каждое изображение проходит через несколько этапов фильтрации и преобразования.
 */

// gen — первый этап конвейера: генерирует числа из переданного слайса
func gen(numbers ...int) <-chan int {
	// Создаем канал для вывода чисел
	out := make(chan int)

	// Запускаем горутину для генерации чисел
	go func() {
		defer close(out) // Закрываем канал после отправки всех чисел
		for _, number := range numbers {
			out <- number // Отправляем каждое число в канал
		}
	}()

	// Возвращаем канал только для чтения
	return out
}

// mul — второй этап конвейера: умножает каждое число на само себя (возведение в квадрат)
func mul(in <-chan int) <-chan int {
	// Создаем выходной канал
	out := make(chan int)

	// Запускаем горутину для обработки входящих чисел
	go func() {
		defer close(out)         // Закрываем выходной канал после обработки всех данных
		for number := range in { // Читаем числа из входного канала
			out <- number * number // Возводим в квадрат и отправляем в выходной канал
		}
	}()

	// Возвращаем выходной канал только для чтения
	return out
}

func main() {
	// Создаем конвейер: gen() -> mul()
	ch := gen(1, 2, 3, 4, 5) // Генерируем числа 1-5

	// Читаем результаты из второго этапа конвейера
	for value := range mul(ch) { // mul получает числа и возвращает их квадраты
		fmt.Println(value) // Выведет: 1, 4, 9, 16, 25
	}
}
