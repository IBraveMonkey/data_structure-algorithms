/*
Hash Table (Хеш-таблица)

Что это такое?
Хеш-таблица — это структура данных, которая реализует абстрактный тип "ассоциативный массив",
связывающий ключи с значениями. Она использует хеш-функцию для вычисления индекса в массиве,
где будет храниться значение, связанное с ключом. Это позволяет эффективно выполнять операции
вставки, поиска и удаления элементов.

Зачем это нужно?
- Быстрый доступ к данным по ключу (в среднем O(1) для операций)
- Эффективное хранение пар ключ-значение
- Группировка и сопоставление данных
- Подсчет частоты элементов

В чём смысл?
- Преобразование ключа в индекс массива с помощью хеш-функции
- Хранение значений в массиве по вычисленным индексам
- Обработка коллизий (когда разные ключи дают одинаковый индекс)
- Быстрый доступ к данным по ключу

Когда использовать?
- Когда нужен быстрый доступ к данным по ключу
- Для подсчета частоты элементов
- Для проверки наличия элемента в наборе
- Для группировки связанных данных

Как работает?
- Ключ преобразуется в индекс с помощью хеш-функции
- Значение сохраняется в массиве по этому индексу
- При поиске ключ снова хешируется, и значение извлекается по индексу
- При коллизиях используются различные методы разрешения (цепочки, открытая адресация)

Как понять, что задача подходит под Hash Table?
- Нужно быстро находить элементы по ключу
- Требуется подсчитать частоту элементов
- Нужно проверить, встречался ли элемент ранее
- Требуется сопоставить связанные данные

Hash Table в Go (map):

В Go хеш-таблицы реализованы в виде встроенной структуры данных - map.
- map[KeyType]ValueType - объявление хеш-таблицы
- Ключи должны быть сравниваемыми типами (int, string, struct и т.д.)
- Значения могут быть любого типа
- Go автоматически управляет размером и коллизиями
- Нет гарантии порядка элементов при итерации
- Потоконебезопасны (нужна синхронизация при конкурентном доступе)

Примеры задач с использованием Hash Table:
Смотрите файл example.go
*/

