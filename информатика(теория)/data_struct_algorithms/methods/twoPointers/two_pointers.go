package two_pointers

/*
Two Pointers (Два указателя)

Что это такое?
Метод двух указателей — это техника решения алгоритмических задач, при которой мы используем два индекса (указателя), движущихся по структуре данных (массиву, строке, списку) с разной скоростью или в разных направлениях.

Зачем это нужно?
- Оптимизация по времени: часто позволяет решить задачу за один проход O(N) вместо вложенных циклов O(N^2).
- Оптимизация по памяти: позволяет решать задачи "на месте" (in-place) с O(1) дополнительной памяти.

В чём смысл?
Мы сужаем область поиска или обрабатываем данные с двух сторон/с разной скоростью, опираясь на свойства данных (например, отсортированность).

Когда использовать?
1. Встречные указатели (Left-Right):
   - Массив отсортирован (найти пару с суммой).
   - Проверка палиндрома.
   - Разворот массива (Reverse).
2. Медленный и быстрый указатели (Slow-Fast / Hare-Tortoise):
   - Поиск цикла в Linked List.
   - Удаление дубликатов из отсортированного массива.
   - Поиск середины списка.
3. Слияние двух отсортированных массивов.

Как работает?
- Left-Right: ставим указатели в начало и конец. Двигаем их навстречу друг другу в зависимости от условия.
- Slow-Fast: оба стартуют сначала. Быстрый идет на 1 или 2 шага, медленный - на 1. Используем разницу в скорости.
*/
