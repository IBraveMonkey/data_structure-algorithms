package sliding_window

/*
Sliding Window (Скользящее окно)

Что это такое?
Метод скользящего окна — это техника оптимизации перебора подмассивов или подстрок.
Вместо того чтобы пересчитывать данные для каждого возможного подмассива заново, мы поддерживаем "окно" (диапазон индексов [left, right]) и обновляем результат при сдвиге окна на шаг.

Зачем это нужно?
- Превратить вложенные циклы (O(N^2) или O(N*K)) в линейный проход O(N).
- Эффективно решать задачи на поиск подстрок, подмассивов с определенной суммой, длиной или уникальностью.

В чём смысл?
- Окно "скользит" по массиву слева направо.
- Правая граница (right) расширяет окно, добавляя новые элементы.
- Левая граница (left) сужается, удаляя старые элементы, когда условие окна нарушается или окно достигает нужного размера.

Когда использовать?
- Дан линейный массив или строка.
- Нужно найти "самый длинный/короткий подмассив", "количество подстрок", "среднее в окне размером K".
- Задача подразумевает непрерывный диапазон элементов.

Как работает?
1. Инициализируем left = 0, currentSum = 0, answer = ...
2. Цикл `right` от 0 до конца:
   - Добавляем arr[right] к текущему состоянию.
   - (Если окно переменного размера) Пока условие нарушено (например, сумма > target):
     - Удаляем arr[left] из состояния.
     - left++.
   - (Если окно фиксированного размера) Если right >= k - 1:
     - Обновляем ответ.
     - Удаляем arr[left] перед следующим шагом и left++.
3. Возвращаем ответ.

### Сложность

| Метрика | Сложность (O) |
|:---|:---:|
| Время | O(N) |
| Память | O(1) / O(K)* |

\*Если нужно хранить состояние окна (например, частоты символов в хеш-таблице).
*/
