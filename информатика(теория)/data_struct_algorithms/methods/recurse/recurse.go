package recurse

/*
Recursion (Рекурсия)

Что это такое?
Рекурсия — это процесс, при котором функция вызывает сама себя как часть своего выполнения.
Это мощный инструмент для решения задач, которые можно разбить на аналогичные подзадачи меньшего размера.

Зачем это нужно?
- Упростить код для сложных задач (например, обход деревьев, графов).
- Решать задачи, определенные индуктивно (факториал, последовательности).
- Заменить сложные вложенные циклы.

В чём смысл?
- Любая рекурсивная функция состоит из двух частей:
  1. Базовый случай (Base Case): условие выхода, когда функция перестает вызывать себя. Без него будет переполнение стека (stack overflow).
  2. Рекурсивный шаг (Recursive Step): вызов функции с новыми аргументами, приближающими нас к базовому случаю.

Когда использовать?
- Структуры данных рекурсивны по природе (Деревья, DOM, JSON).
- Алгоритмы "Разделяй и властвуй" (Merge Sort, Quick Sort).
- Динамическое программирование и Backtracking.

Как работает?
При каждом вызове функции создается новый контекст в стеке вызовов (Call Stack). Когда базовый случай достигнут, стек начинает "сворачиваться", возвращая результаты обратно.

Глубокая рекурсия может привести к переполнению стека. В некоторых языках есть оптимизация хвостовой рекурсии, но в стандартном Go - нет.

### Сложность

| Метрика | Сложность (O) |
|:---|:---:|
| Время | Зависит от количества вызовов |
| Память | O(d) |

\*d — максимальная глубина рекурсии (глубина стека вызовов).
*/
