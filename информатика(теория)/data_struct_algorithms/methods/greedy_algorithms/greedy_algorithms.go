package greedy_algorithms

/*
Greedy Algorithms (Жадные алгоритмы)

Что это такое?
Жадный алгоритм — это подход к решению оптимизационных задач, при котором на каждом шаге делается локально оптимальный выбор, с надеждой, что эти шаги приведут к глобально оптимальному решению.

Зачем это нужно?
- Для быстрого решения задач оптимизации (найти минимум/максимум).
- Когда полный перебор или DP слишком медленны, а жадный подход дает точное (или хорошее приближенное) решение.

В чём смысл?
- Мы не смотрим в будущее и не откатываемся назад (в отличие от DP и Backtracking).
- Принимаем решение "здесь и сейчас" на основе текущей ситуации.

Когда использовать?
- Когда задача обладает свойством "жадного выбора" (greedy choice property): локальный оптимум ведет к глобальному.
- Задача имеет "оптимальную подструктуру" (как и DP).
- Примеры: поиск кратчайшего пути (Дейкстра), сжатие Хаффмана, построение остовного дерева (Прим, Краскал), задачи о расписании.

Как работает?
1. Сортируем данные (часто жадность основана на порядке: самый дешевый, самый короткий, самый ранний и т.д.).
2. Итерируемся и берем элемент, если он улучшает решение и допустим.
3. Повторяем до конца.

Внимание!
Жадность работает не везде. Простой пример сбоя - сдача монетами произвольных номиналов (например, сдача 6 монетами 4, 3, 1: жадный (4+1+1) даст 3 монеты, а оптимум (3+3) - 2 монеты).
*/
