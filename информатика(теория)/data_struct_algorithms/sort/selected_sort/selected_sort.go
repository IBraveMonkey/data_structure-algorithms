package selected_sort

/*
Selection Sort (Сортировка выбором)

Что это такое?
Сортировка выбором — это алгоритм, который делит массив на отсортированную и неотсортированную части.
Он многократно находит минимальный элемент из неотсортированной части и ставит его в конец отсортированной.

Зачем это нужно?
- Это один из самых простых алгоритмов для понимания.
- Он совершает минимальное количество записей (swaps) — ровно N (или N-1), что может быть полезно, если запись в память очень дорогая (например, write to Flash).

В чём смысл?
- "Найди самый маленький, поставь первым. Найди следующий самый маленький, поставь вторым..."

Когда использовать?
- В учебных целях.
- Когда количество "обменов" (write operations) критично (хотя Cycle Sort делает их еще меньше, Selection Sort делает O(N)).
- В реальной жизни почти всегда проигрывает Insertion Sort или Quick Sort.

Как работает?
1. В цикле от 0 до N-1:
   - Ищем индекс минимума в подмассиве [i...N-1].
   - Делаем swap(arr[i], arr[min]).

Сложность:
- Время: O(n^2) ВСЕГДА (даже если массив уже отсортирован).
- Память: O(1).
- Устойчивость: Неустойчив (Unstable), так как длинные прыжки при обмене могут нарушить порядок равных элементов.
*/

func SelectedSort(arr []int) {
	n := len(arr)
	for i := 0; i < n-1; i++ {
		minIdx := i
		// Ищем минимум в оставшейся части
		for j := i + 1; j < n; j++ {
			if arr[j] < arr[minIdx] {
				minIdx = j
			}
		}
		// Меняем местами, если нашли новый минимум
		if minIdx != i {
			arr[i], arr[minIdx] = arr[minIdx], arr[i]
		}
	}
}
