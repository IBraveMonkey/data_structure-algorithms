/*
Strings (Строки) в Go

Что это такое?
Строка в Go — это неизменяемая (immutable) последовательность байтов.
Строки в Go обычно содержат текст в кодировке UTF-8.
В отличие от многих других языков, строки в Go — это не массив символов, а скорее "срез" байтов, доступный только для чтения.

Внутреннее устройство:
Строка в Go — это структура, состоящая из двух полей:
1. Указатель на массив байтов (где хранятся данные).
2. Длина строки (количество байтов).

Ключевые особенности:
- Неизменяемость: Нельзя изменить символ в строке по индексу (str[0] = 'a' вызовет ошибку).
- UTF-8 по умолчанию: Go изначально спроектирован для работы с UTF-8.
- Rune (Руна): Для работы с Unicode символами используется тип rune (alias для int32).
  Один символ (руна) может занимать от 1 до 4 байтов.

Эффективность:
- Копирование строки дешево, так как копируется только заголовок (указатель + длина), а не сами данные.
- Операция получения подстроки (slicing) также дешева и не требует выделения новой памяти (используется тот же массив байтов).

Основные операции:
- len(s): возвращает количество БАЙТОВ, а не символов.
- []rune(s): преобразование в массив рун для корректной работы с мультибайтными символами.
- range s: итерируется по рунам (символам), а не байтам.

Когда использовать?
- Для работы с любым текстовым контентом.
- Когда нужны гарантии неизменяемости данных.
- В качестве ключей в map (так как они сравнимы).

Примеры задач на строки:
См. файл example.go
*/

package strings

import "fmt"

// StringInfo - демонстрационная функция для показа свойств строк
func StringInfo() {
	str := "Привет, World"

	fmt.Printf("Строка: %s\n", str)
	fmt.Printf("Длина (байты): %d\n", len(str))
	fmt.Printf("Количество символов (руны): %d\n", len([]rune(str)))

	fmt.Println("Итерация по байтам:")
	for i := 0; i < len(str); i++ {
		fmt.Printf("%x ", str[i])
	}
	fmt.Println()

	fmt.Println("Итерация по рунам (символам):")
	for idx, r := range str {
		fmt.Printf("%d:%c ", idx, r)
	}
	fmt.Println()
}
