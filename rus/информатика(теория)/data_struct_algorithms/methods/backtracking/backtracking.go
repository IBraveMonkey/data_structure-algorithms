package backtracking

/*
Backtracking (Поиск с возвратом)

Что это такое?
Backtracking — это метод поиска решения путем построения кандидатов поэтапно и отказа от кандидата ("backtrack" - возврат назад), как только становится ясно, что этот кандидат не может быть частью правильного решения. Другими словами, это систематический перебор вариантов с отсечением тех, которые заведомо не подходят.

Зачем это нужно?
- Для решения комбинаторных задач (перестановки, комбинации).
- Для задач, где нужно найти ВСЕ возможные решения (например, N ферзей, Судоку).
- Когда прямой перебор слишком дорог, и нужно отсекать ветви дерева поиска.

В чём смысл?
- Мы как бы ходим по лабиринту: идем вперед, пока можем. Если уперлись в тупик - возвращаемся до развилки и выбираем другой путь.
- Использует рекурсию для углубления в дерево решений.

Когда использовать?
- Задачи на нахождение всех решений.
- Головоломки (Судоку, Кроссворды).
- Задачи размещения (N ферзей).
- Генерация всех возможных паролей, подмножеств.

Как работает?
1. Выбираем вариант решения (например, ставим ферзя в клетку).
2. Проверяем, допустимо ли это (не бьют ли его другие).
3. Если допустимо - переходим к следующему шагу (ставим следующего ферзя).
4. Если недопустимо или пришли в тупик - отменяем выбор (убираем ферзя) и пробуем следующий вариант.

### Сложность

| Метрика | Сложность (O) |
|:---|:---:|
| Время | O(b^d) / O(N!) |
| Память | O(d) |

\*b — коэффициент ветвления (branching factor), d — глубина дерева решений.
\*\*Сложность сильно зависит от конкретной задачи и эффективности отсечения ветвей.
*/
