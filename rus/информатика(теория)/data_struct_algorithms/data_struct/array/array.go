/*
Массив (Array) и Срез (Slice)

Что это такое?
Массив — это структура данных фиксированного размера, содержащая элементы одного типа, расположенные в непрерывной области памяти.
В Go чаще используются Срезы (Slices) — это динамические массивы, которые могут изменять свой размер.

Зачем это нужно?
- Для хранения упорядоченных списков данных одного типа.
- Когда требуется быстрый доступ к элементам по индексу.
- Для обеспечения высокой производительности за счетПрямой доступ к элементам
- Вставка и удаление элементов
- Динамическое расширение (через append)

### Сложность

| Операция | Временная сложность (O) | Пространственная сложность (O) |
|:---|:---:|:---:|
| Доступ (по индексу) | O(1) | O(1) |
| Вставка (в конец) | O(1) амортизированная | O(1) |
| Вставка (в середину) | O(n) | O(1) |
| Удаление (из конца) | O(1) | O(1) |
| Удаление (из середину) | O(n) | O(1) |
| Поиск (неотсортированный) | O(n) | O(1) |
| Хранение | — | O(n) |

В чём смысл?
- Понимание работы слайсов (срез) в Go
- Эффективное использование памяти
- Правильный выбор между массивом и слайсом
эффективно предсказывать следующие данные (cache-friendly).
- Прямой доступ к любому элементу по формуле: адрес = начало + индекс * размер_элемента.

Когда использовать?
- Когда количество элементов известно заранее или меняется нечасто.
- Когда важна скорость чтения/записи по индексу (O(1)).
- Когда данные должны обрабатываться последовательно.

Сложность операций:
- Доступ по индексу: O(1)
- Поиск (неотсортированный): O(n)
- Поиск (отсортированный, бинарный): O(log n)
- Вставка (в начало/середину): O(n) — требует сдвига элементов
- Вставка (в конец): O(1) (амортизированная для срезов)
- Удаление: O(n) — требует сдвига элементов

Как понять, что задача подходит под Массив/Срез?
- Данные представляют собой простую последовательность.
- Нужно часто обращаться к элементам по их порядковому номеру.
- Память должна быть выделена одним целым блоком для эффективности.
*/

package array

import "fmt"

// ArrayExamples демонстрирует базовые операции с массивами и срезами
func BasicOperations() {
	// 1. Объявление массива (фиксированный размер)
	var arr [5]int
	arr[0] = 10 // O(1)

	// 2. Объявление среза (динамический массив)
	slice := []int{1, 2, 3}

	// 3. Добавление в конец (Append)
	// В среднем O(1), но если капасити (capacity) исчерпано,
	// произойдет переаллокация и копирование массива (O(n)).
	slice = append(slice, 4)

	// 4. Вставка в середину (O(n))
	index := 1
	value := 99
	slice = append(slice[:index+1], slice[index:]...)
	slice[index] = value

	// 5. Удаление из середины (O(n))
	// Сохраняем порядок, сдвигая элементы
	slice = append(slice[:index], slice[index+1:]...)

	fmt.Println("Итоговый срез:", slice)
}
