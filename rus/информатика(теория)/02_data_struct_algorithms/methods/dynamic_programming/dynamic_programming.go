package dynamic_programming

/*
Dynamic Programming (Динамическое программирование)

Что это такое?
Динамическое программирование — это метод решения сложных задач путём разбиения их на более простые подзадачи. Ключевая особенность в том, что решения подзадач запоминаются (мемоизация или табуляция), чтобы не вычислять их повторно при перекрывающихся вычислениях.

Зачем это нужно?
- Ускорить решение задач с экспоненциальной сложности до полиномиальной.
- Избежать повторных вычислений (например, как в наивной рекурсии Фибоначчи).
- Найти глобальный оптимум (максимум/минимум) в задачах оптимизации.

В чём смысл?
- Разделяем задачу на маленькие шаги (подзадачи).
- Сохраняем ответы (в массиве, хеш-таблице или матрице).
- Строим решение "снизу вверх" (bottom-up) или "сверху вниз" (top-down) с кешированием.

Когда использовать?
- Есть "оптимальная подструктура" (решение большой задачи состоит из оптимальных решений маленьких).
- Есть "перекрывающиеся подзадачи" (одни и те же подзадачи решаются многократно).
- Задачи на поиск экстремума (макс. прибыль, мин. путь) или количества вариантов.

Как работает?
1. Определяем состояние (параметры подзадачи, например, i-й элемент, вес w).
2. Записываем уравнение перехода (рекуррентное соотношение), связывающее текущее состояние с предыдущими.
3. Задаем базовые случаи (начальные условия).
4. Заполняем таблицу (при итеративном подходе) или используем рекурсию с кешом.

### Сложность

| Метрика | Сложность (O) |
|:---|:---:|
| Время | O(N * M...) |
| Память | O(N * M...) |

\*N, M — размеры таблицы состояний (State Space).
**Память может быть оптимизирована до O(N) в ряде случаев.
*/
